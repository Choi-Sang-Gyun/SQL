--ORDER BY구문
SELECT * FROM EMPLOYEES ORDER BY SALARY; -- ASC가 디폴트
SELECT * FROM EMPLOYEES ORDER BY SALARY DESC;
SELECT * FROM EMPLOYEES ORDER BY DEPARTMENT_ID, SALARY DESC; --DEPARTMENT_ID 기준으로 먼저 정렬, 그 안에서 SALARY기준으로 정렬(근데 DESC니까 내림차순으로

SELECT FIRST_NAME, SALARY*12 AS 연봉 FROM EMPLOYEES ORDER BY 연봉 DESC; --엘리어스를 정렬에서 사용할 수 있다
SELECT FIRST_NAME, SALARY*12 AS 연봉 FROM EMPLOYEES WHERE 연봉 >= 10000; --안되는 거임 WHERE 사용 X

SELECT FIRST_NAME, SALARY, DEPARTMENT_ID FROM EMPLOYEES WHERE DEPARTMENT_ID = 50 ORDER BY FIRST_NAME;











